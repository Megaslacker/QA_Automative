public class Square {
    public int getPeriment(int a){
        return a*4;
    }
public int getSquare(int b){
        return b*b;
}
public double getPeriment(double pi, int radius){
        return 2*pi*radius;
}
    public double getSquare(double pi, int radius){
        return pi*radius*radius;
    }
    public int getPeriment(int a, int b, int c){
        return a+b+c;
    }
    public double getSquare(int b, int h){
        return b*h*0.5;
    }
}

